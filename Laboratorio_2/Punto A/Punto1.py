    def Suma(self):
        a = self.inputA.text()
        b = self.inputB.text()
        c = float(a) + float(b)
        self.operacion.setText("+")
        self.resultadoA.setText(str(c))

    def Resta(self):
        a = self.inputA.text()
        b = self.inputB.text()
        c = float(a) - float(b)
        self.operacion.setText("-")
        self.resultadoA.setText(str(c))

    def Multiplicacion(self):
        a = self.inputA.text()
        b = self.inputB.text()
        c = float(a) * float(b)
        self.operacion.setText("x")
        self.resultadoA.setText(str(c))

    def Cociente(self):
        a = self.inputA.text()
        b = self.inputB.text()
        c = float(a) / float(b)
        self.operacion.setText("/")
        self.resultadoA.setText(str(c))

    def Residuo(self):
        a = self.inputA.text()
        b = self.inputB.text()
        c = float(a) % float(b)
        self.operacion.setText("%")
        self.resultadoA.setText(str(c))

    def Sen(self):
        d = self.inputC.text()
        e = math.sin(math.radians(int(d)))
        self.resultadob.setText(str(e))
        

    def Cos(self):
        d = self.inputC.text()
        e = math.cos(math.radians(int(d)))
        self.resultadob.setText(str(e))

    def Tan(self):
        d = self.inputC.text()
        e = math.tan(math.radians(int(d)))
        self.resultadob.setText(str(e))

    def Ctn(seslf):
        d = seslf.inputC.text()
        e = (1 / math.tan(math.radians(int(d))))
        seslf.resultadob.setText(str(e))

    def Sec(seslf):
        d = seslf.inputC.text()
        e = (1 / math.cos(math.radians(int(d))))
        seslf.resultadob.setText(str(e))

    def Csc(seslf):
        d = seslf.inputC.text()
        e = (1 / math.sin(math.radians(int(d))))
        seslf.resultadob.setText(str(e))


